# Exercise 07: Some Counting
# This program performs various counting tasks using for loops.

# First loop: Count from 0 to 49
for a in range(50):
    print(a)
else:
    print()

# Second loop: Count from 50 down to 0
for b in range(50, -1, -1):
    print(b)
else:
    print()

# Third loop: Count from 30 to 49
for c in range(30, 50):
    print(c)
else:
    print()

# Fourth loop: Count from 50 down to 11, stepping by 2
for d in range(50, 10, -2):
    print(d)
else:
    print()

# Fifth loop: Count from 100 to 199, stepping by 5
for e in range(100, 200, 5):
    print(e)
else:
    print()
